# Operators
print(4 + 8)   # addition(+)
print(13 - 9)   # subtraction(-)
print(5 * 3)   # multiplication(*)
print(8 / 2)   # division(/)
print(5 ** 2)  # exponential(**)
print(8 % 3)   # modulus(%)
print(8 // 2)  # Floor division operator(//)


# Checking data types
print(type(8))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Bagga'))          # String
print(type([1, 2, 3]))           # List
print(type({'name':'Bagga'})) # Dictionary
print(type({8.8, 3.14, 3.6}))    # Set
print(type((4.8, 7.25, 1.8)))    # Tuple